#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int		i;
	int		j;
	int		k;
	char	*copy;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	if (s[i] == '\0')
		return (ft_strcpy(ft_memalloc(sizeof(char) * 2), ""));
	j = ft_strlen(s) - 1;
	while (s[j] == ' ' || s[j] == '\n' || s[j] == '\t')
		j--;
	copy = (char *)malloc(sizeof(char) * (j - i + 2));
	if (!copy)
		return (NULL);
	k = -1;
	while (++k < j - i + 1)
		copy[k] = s[i + k];
	copy[k] = '\0';
	return (copy);
}
